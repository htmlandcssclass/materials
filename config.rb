helpers do
  def title
    current_page.data.title || @title || nil
  end

  def breadcrumbs
    return [
      { id: 'helloworld', title: 'Hello, World!'},
      { id: 'tron', title: 'Tron'}
    ]
  end

  def link_to_crumb(crumbs)
    return link_to(crumbs[:title], crumbs[:id])
  end
end

